# 02 Tekstin tulostaminen ja lähdekoodin kommentointi

# Tämä rivi ei tulostu koska on kommentti.
#
# Usean rivin kommentointi on mahdollista
# usella risuaidalla.
print("Tämä on tulostettavaa tekstiä.")

# Usean rivin kommentointi on myös mahdollista käyttäen
# kolmea peräkkäistä lainausmerkkiä.
# Tämän jälkeen voit rivittää tekstin miten haluat ja lopuksi
# muista päättää usean rivin kommentti kolmeen lainausmerkkiin.
""" Tästä alkaa usean rivin kommentointi.
Ei tarvitse enää laittaa risuaitaa rivin eteen
vaan riittää pelkkä rivinvaihto.
"""
print("Tämä on toinen tulostettava rivi.")
print("Kolmas rivi") # Kommentti voi sijaita myös rivin perässä.